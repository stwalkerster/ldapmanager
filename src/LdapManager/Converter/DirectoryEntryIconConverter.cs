namespace LdapManager.Converter
{
    using System;
    using System.Globalization;
    using System.Windows.Data;

    using LdapManager.Models;

    /// <summary>
    /// The directory entry icon converter.
    /// </summary>
    internal class DirectoryEntryIconConverter : IValueConverter
    {
        #region Public Methods and Operators

        /// <summary>
        /// The convert.
        /// </summary>
        /// <param name="value">
        /// The value.
        /// </param>
        /// <param name="targetType">
        /// The target type.
        /// </param>
        /// <param name="parameter">
        /// The parameter.
        /// </param>
        /// <param name="culture">
        /// The culture.
        /// </param>
        /// <returns>
        /// The <see cref="object"/>.
        /// </returns>
        public object Convert(object value, Type targetType, object parameter, CultureInfo culture)
        {
            var entry = value as DirectoryEntry;
            if (entry == null)
            {
                throw new ArgumentNullException("value");
            }

            if (entry.IsBase)
            {
                return "pack://application:,,,/LdapManager;component/Images/16x16/folder_home.png";
            }

            if (!entry.HasBeenResolved)
            {
                return "pack://application:,,,/LdapManager;component/Images/16x16/folder_grey.png";
            }

            return "pack://application:,,,/LdapManager;component/Images/16x16/folder.png";
        }

        /// <summary>
        /// The convert back.
        /// </summary>
        /// <param name="value">
        /// The value.
        /// </param>
        /// <param name="targetType">
        /// The target type.
        /// </param>
        /// <param name="parameter">
        /// The parameter.
        /// </param>
        /// <param name="culture">
        /// The culture.
        /// </param>
        /// <returns>
        /// The <see cref="object"/>.
        /// </returns>
        /// <exception cref="NotImplementedException">
        /// Not implemented
        /// </exception>
        public object ConvertBack(object value, Type targetType, object parameter, CultureInfo culture)
        {
            throw new NotImplementedException();
        }

        #endregion
    }
}